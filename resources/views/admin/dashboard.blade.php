    <script>
        // Mobile menu toggle
        function mobileMenu() {
            const loginIconToggle = document.querySelector('.mobileMenuOpen');
            if (loginIconToggle.classList.contains('-translate-x-full') && loginIconToggle.classList.contains(
                    'sm:translate-x-0')) {
                loginIconToggle.classList.remove('-translate-x-full');
                loginIconToggle.classList.remove('sm:translate-x-0');
            } else {
                loginIconToggle.classList.add('-translate-x-full');
                loginIconToggle.classList.add('sm:translate-x-0');
            }
        }

        // user Menu toggle
        function userMenu() {
            const userMenu = document.querySelector('.userMenu')
            userMenu.classList.toggle('hidden');
        }
    </script>
